#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	n;

	if (nb < 10 && nb > -10)
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		n = '0' + nb;
		write(1, &n, 1);
		return ;
	}
	ft_putnbr(nb / 10);
	nb %= 10;
	if (nb < 0)
		nb *= -1;
	n = '0' + nb;
	write(1, &n, 1);
}
